	/**
	 * @return Net_GeoIP
	 */
	protected function getGeoInstance() {
		if (self::$geo === null) {
			self::$geo = false;
			
			$file = Mage::getStoreConfig('worldmap/geoip/filename');
			$path = Mage::getBaseDir('var') . DS . $file;
			
			if (!@class_exists('Net_GeoIP')) {
				Mage::getModel('Pz_WorldMap_Model_Observe')->initRoutes();
			}
			
			if (is_file($path)) {
				self::$geo = Net_GeoIP::getInstance($path, Net_GeoIp::STANDARD);
			} else {
				Mage::throwException("GeoIP database file not found");
			}
		}
		return self::$geo;
	}
	
	/**
	 * check for private address
	 * 
	 * @param string $ip
	 * @return bool
	 */
	public function isPrivateIpAddress($ip) {
		$num = ip2long($ip);
		if ($num >= 2130706432 && $num <= 2147483647) {
			// 127.0.0.0 - 127.255.255.255
			return true;
		} elseif ($num >= 167772160 && $num <= 184549375) {
			// 10.0.0.0 - 10.255.255.255
			return true;
		} elseif ($num >= -1408237568 && $num <= -1407188993) {
			// 172.16.0.0 - 172.31.255.255
			return true;
		} elseif ($num >= -1062731776 && $num <= -106266624) {
			// 192.168.0.0 - 192.168.255.255
			return true;
		} else {
			return false;
		}
	}
	
	/**
	 * Parse IP Location information
	 * 
	 * @param string $ip
	 * @return array
	 */
	public function parse($ip) {
		$result = array('ip'=> $ip, 'longitude'=> 360, 'latitude'=> 360, 'location'=> '');
		
		if (isset($_REQUEST['debug'])) {
			$result['country'] = 'US';
			$result['region'] = 'CA';
			$result['city'] = 'Los Angne';
			return $result;
		}
		$data = array('ip'=> $ip);
		
		try {
			if ($this->isPrivateIpAddress($ip)) {
				$var = false;
				$url = 'http://zosoft.net/api/rest/query/default/geo/';
				if (defined('CURLOPT_FOLLOWLOCATION')) {
					$options = array(
							'adapter'=> 'Zend_Http_Client_Adapter_Curl', 
							'timeout'=> 30, 
							'curloptions'=> array(CURLOPT_FOLLOWLOCATION=> true));
				} else {
					$options = array('timeout'=> 30);
				}
				$client = new Zend_Http_Client($url, $options);
				$response = $client->request();
				if ($response->getStatus() == 200) {
					$json = $response->getBody();
					$val = @Zend_Json::decode($json);
				}
				if ($val && isset($val['state']) && $val['state']) {
					$raw = array_keys($val['result']);
					
					if ($raw) {
						$ip = $raw[0];
						$data = $val['result'][$ip];
						$data['ip'] = $ip;
					}
				}
			} else {
				$geo = $this->getGeoInstance();
				if ($geo) {
					$location = $geo->lookupLocation($ip);
					if ($location) {
						$keys = array_keys($location->getData());
						foreach ($keys as $key) {
							$data[$key] = $location->$key;
						}
					}
				}
			}
		} catch (Exception $e) {
			// echo $e->getMessage();
		}
		
		if (isset($data['longitude'])) {
			$result['longitude'] = $data['longitude'];
			$result['latitude'] = $data['latitude'];
			
			$result['country'] = $data['countryCode'];
			$result['countryName'] = Mage::app()->getLocale()->getCountryTranslation($result['country']);
			
			$result['region'] = isset($data['region']) ? $data['region'] : '';
			$result['city'] = isset($data['city']) ? $data['city'] : '';
			$result['postcode'] = isset($data['postalCode']) ? $data['postalCode'] : '';
			
			if (isset(self::$nameMap[$result['country']][$result['region']])) {
				$result['regionName'] = self::$nameMap[$result['country']][$result['region']];
			} else {
				$result['regionName'] = '';
			}
			
			if ($result['country'] && !in_array($result['country'], array('CA', 'US'))) {
				if (isset(self::$fipsMap[$result['country']][$result['region']])) {
					$result['region'] = self::$fipsMap[$result['country']][$result['region']];
				}
			}
			
			$address = array();
			foreach (array('city', 'regionName', 'postcode', 'countryName') as $key) {
				if (isset($result[$key]) && $result[$key]) {
					$address[] = trim($result[$key]);
				}
			}
			$result['location'] = implode(', ', $address);
			
			$result['raw'] = $data;
		}
		return $result;
	}
	
	/**
	 * Get country name
	 * 
	 * @param string $code
	 * @return string
	 */
	public function getCountryName($code) {
		return Zend_Locale::getTranslation($code, 'country');
	}
	
	/**
	 * Get region name
	 * 
	 * @param string $regionCode
	 * @param string $countryCode
	 * @return string
	 */
	public function getRegionName($regionCode, $countryCode) {
		global $GEOIP_REGION_NAME;
		if (isset($GEOIP_REGION_NAME[$countryCode][$regionCode])) {
			return $GEOIP_REGION_NAME[$countryCode][$regionCode];
		}
		return null;
	}
	
